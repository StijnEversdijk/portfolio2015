      <h3>Extending the brand</h3>
      <p>
        With the Kuvva-project we worked in a team varying, over time, from 4 to 8 people. My main focus within the project would be the surrounding ‘communications’.
      </p>

      <p>
        To stay relevant within the community, we wanted to share good content with our audience. This meant we needed a blog, but also a Tumblr. And how do we communicate concerning our apps, which would still have the same functionality? My task was to discuss this, design the interfaces and implement it.
      </p>

      <p>
        Besides this all, the main product was also in development. How do you design in a same style, if this style is still being designed? That Kuvva wasn’t a finished product, while I was working on it, made it even more fun. This way my designs would influence the endproduct. Substantial parts, such as typography, draw inspiration from the blog and vice versa.
      </p>
