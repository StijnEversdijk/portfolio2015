  <section class="text">
    <p>
      From the start, we all had a different responsibility within the team. Mine was prototyping. Quickly we noticed that we were working on our own island, and not really communicating. We decided to change the roles. We split up in two; one team being responsible for everything concerning interaction and one team for everything visual.
    </p>

    <img src="img/iuxd/product-statement.jpg" />
    <span class="caption">Starting off the sprint with a product&nbsp;statement.</span>
    <p>
      I would work together with one teammate on the interaction team. We drastically changed our workflow, to get the most out of the time we had together. Instead of making enormous amounts of wireframes, we started with a small sketch of a page. We created the sketch while discussing it with each other. Then, we would rapidly work out the wireframe, or set of wireframes, in Illustrator. We would run to the printer, print the wireframes you just created in 10 - 15 minutes. You give every team member a big marker and start discussing everything you notice, within a maximum amount of time. This would lead to new sketches and wireframes and we would iterate multiple times, before coming to a final solution.
    </p>

    <img src="img/iuxd/workflow@2x.png" />
    <span class="caption">Our workflow during the wireframe-proces;<br />Sketch, feedback, iterate&nbsp;and&nbsp;deliver.</span>

    <p>
      Next to the interaction design, we were also responsible for the prototypes. Within a project you have a lot of different aspects to prototype. For the overall flow, we created an easy InVision prototype. Small interactions were prototyped with Framer, which would be much more time-expensive, but the focus on animation and really being able to play around with your design made it really valuable.
    </p>
    <img src="img/iuxd/arduino.jpg" class="standing" />
    <p>
      Prototyping within this project varied from creating storyboards, making motion dummies to work out prototypes with code.
    </p>
