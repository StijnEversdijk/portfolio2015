    <h4>Designing the user interface</h4>

    <p>
      The minor programme was created to let us work on a project for a half schoolyear and design our own ‘ideal’ studio environment. The period will be separated in two phases; a Research &amp; Concept phase and a Creation phase. Both will take 8 weeks of intensive work.
    </p>

    <p>
      The project was divided in two phases; a Research &amp; Concept phase and a Creation Phase. We were working in a group of four, where everyone would be responsible for one of the four roles; Visual Design, Prototyping, Interaction Design and Concept/Strategy/Planning. Mine would be Prototyping.
    </p>


    <h4>
      From briefing to defrief
    </h4>

    <p>
      The programme started of with a brief from our client, IceMobile; a digital agency from Amsterdam, specialised in the retail branche. With help of a kick off workshop, we tried to find the expectations our client has of us.
    </p>


    <h4>
      Exploratory research with quantative&nbsp;data
    </h4>

    <p>
      During our project, we would focus on food, and combining this with the fun-factor. We had no clue where to start or what the context looked like. The assignment was very broad, so we could focus on any subject related to food; obesity, lack of time to cook, recipes, healthy eating, you name it.
    </p>

    <p>
      To get a grasp on what we were dealing with, we started out with mapping our assumptions. All we needed for this were some post-its and a whiteboard. Our goal was to bust as many of those assumptions with our following research. After this, we conducted a survey. This provided us with some basic insights and possible angles for our project.
    </p>
    <img src="img/iuxd/enquete-resultaten2@2x.png" />
    <p>
      We created a stakeholder map, and based on the information we had so far, we were ready to get in touch with potential target audiences.
    </p>

    <img src="img/iuxd/stakeholdersmap@2x.png" />

    <h4>Going in depth</h4>
    <p>
      In six days, we gathered as much information as we could. Our main research techniques was the creation of customer journeys with the respondents. This would give us a good view of both the ‘before’, ‘during’ and ‘after’ situations.
    </p>

    <img src="img/iuxd/journeymapping.jpg" />

    <p>
      We wrote all of our raw data and insights onto post-its, and started grouping them to see if there are any similarities or opposing statements in the data, which we could use as values for our archetype axis. We also created four target groups which we could further explore by putting them somewhere on the axis of the archetype systems.
    </p>

    <img src="img/iuxd/archetype_axis@2x.png" class="small" />
    <span class="caption">Archetypes mapped on different axis, to show difference between the archetypes</span>
    <p>
      Last, but not least, we used acquired information about the target groups from the archetype system to create a very complete customer journey map which displayed not only the actions, but also the feelings , thoughts, and corresponding platforms at every moment of the customer journey.
    </p>

    <p>
      We presented our insights to the client. Based on the discussion with our client, we picked a target audience for our project.
    </p>

