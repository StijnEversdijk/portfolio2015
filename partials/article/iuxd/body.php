    <h4>Concept Phase</h4>

    <p>
      We started off quick, and immediately started creating concepts. We noticed this was not the way we should approach this phase. We found out that we still didn’t know enough about our final target audience, so we gave ourselves one extra week to do research.
    </p>

    <p>
      After having gathered a lot of useful insights, we started again. We used different creative methods to facilitate the brainstorm sessions. We brainstormed on different keywords and used associative techniques.
    </p>

    <p>
      We had a longlist of around 20 concepts. We narrowed this down to three. To validate our concepts, we quickly made a storyboard. We would then go to our target audience and discuss our newborn concepts.
    </p>
    <img src="img/iuxd/storyboarding.jpg" />
    <p>
      We wanted to give our concepts a bit more body, so we started thinking of scenarios in which you would use our concept, the problems the concept should tackle and features the concept should have.
    </p>

    <p>
      To get our concepts to a next fidelity, we created motion dummies. We noticed that words are just words. A visual will make everything much clearer, when you have discuss your idea. To get a grasp on our concepts, we even acted out our concept with a role-play, just to gain knowledge.
    </p>

    <p>
      We presented to our client, and we picked one concept to work on in the creation phase. When we came back to our desks, we noticed that this concept wasn’t the one. Somehow, we altered an old concept we killed early on in the proces, scheduled a Skype-call with the client and we realised that this was the concept we should go for.
    </p>

    <h4>Creation phase</h4>

    <p>
      Next up was the creation phase, for a period of eight weeks. We all had some experience working on a project, but we never did a project in an agile way, as it tells you in the books. We took a go through the book ‘Get Agile’, create user stories, a product statement, play planning poker and create sprint goals. The create-phase contained three sprints; one of three weeks and two sprints of two weeks.
    </p>
  </section>
