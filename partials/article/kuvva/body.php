      <p>
          With the enormous database of illustrations and users and the authority they gained by curating and highlighting the best illustrations, Kuvva was ready for a pivot. We would start selling licenses, so the illustrations could be used in campaigns, magazines and advertisements on a legal basis. We would cut out the middle man and empower both the client as the illustrator. All that, without losing interest from our daily users, who were only looking for beautiful illustrations.
      </p>

      <p>
          This meant completely rethinking Kuvva; a new identity, a redesign of our website, a new strategy et cetera.
      </p>

