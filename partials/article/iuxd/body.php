    <p>
      During a sprint you keep learning. Especially the moment when we realised that we were trying to over-deliver within one sprint. We decided to cut out some features, or that they wouldn’t be a part of out MVP. I spent two days working with an Arduino, just to realise that this wasn’t the way to go. The benefit of our flexible team was that it was easy to find a solution for this.
    </p>

    <p>
      At the moment, we are still working on this project. We are trying to improve parent-child interaction through cooking. Cooking should be fun, but can also be educational. Both parent and child can benefit from this. For us, this was a real challenge, because the target group couldn’t be further away. This also made it exciting and made our research even more valuable.
